package lint

import (
	"apm/internal/common/reply"
	"bufio"
	"context"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
)

type TmpFilesEntry struct {
	Type  string // "d" (directory), "L" (symlink)
	Path  string
	Mode  string
	User  string
	Group string
	Line  string // Полная строка в формате tmpfiles.d
}

type TmpFilesAnalysis struct {
	Missing     []TmpFilesEntry   // Записи без покрытия tmpfiles.d
	Unsupported []string          // Файлы, которые нельзя выразить через tmpfiles.d
	Existing    map[string]string // Текущие записи tmpfiles.d (путь -> строка)
}

func (a *TmpFilesAnalysis) Analyze(ctx context.Context, rootfs string) error {
	reply.CreateEventNotification(ctx, reply.StateBefore, reply.WithEventName(reply.EventSystemLintTmpfiles))
	defer reply.CreateEventNotification(ctx, reply.StateAfter, reply.WithEventName(reply.EventSystemLintTmpfiles))
	existing, err := a.readEntries(rootfs)
	if err != nil {
		return fmt.Errorf("reading tmpfiles.d: %w", err)
	}
	a.Existing = existing

	varPath := filepath.Join(rootfs, "var")
	if _, err = os.Lstat(varPath); os.IsNotExist(err) {
		return nil
	}

	if err = a.walk(rootfs, varPath); err != nil {
		return fmt.Errorf("walking /var: %w", err)
	}

	sort.Slice(a.Missing, func(i, j int) bool {
		return a.Missing[i].Path < a.Missing[j].Path
	})
	sort.Strings(a.Unsupported)
	return nil
}

func (a *TmpFilesAnalysis) GenerateConf() string {
	var b strings.Builder
	b.WriteString("# Auto-generated by apm lint\n")
	for _, e := range a.Missing {
		b.WriteString(e.Line)
		b.WriteByte('\n')
	}
	return b.String()
}

// WriteConf записывает сгенерированный конфиг в rootfs/usr/lib/tmpfiles.d/apm-lint.conf
func (a *TmpFilesAnalysis) WriteConf(rootfs string) (string, error) {
	if len(a.Missing) == 0 {
		return "", nil
	}
	return writeConf(rootfs, "tmpfiles.d", a.GenerateConf())
}

func (a *TmpFilesAnalysis) readEntries(rootfs string) (map[string]string, error) {
	entries := make(map[string]string)

	// usr/lib — меньший приоритет, etc переопределяет
	for _, sub := range []string{"usr/lib/tmpfiles.d", "etc/tmpfiles.d"} {
		dir := filepath.Join(rootfs, sub)
		if _, err := os.Stat(dir); os.IsNotExist(err) {
			continue
		}

		files, err := os.ReadDir(dir)
		if err != nil {
			return nil, fmt.Errorf("reading %s: %w", dir, err)
		}

		for _, f := range files {
			if f.IsDir() || !strings.HasSuffix(f.Name(), ".conf") {
				continue
			}
			if err = a.parseFile(filepath.Join(dir, f.Name()), entries); err != nil {
				return nil, err
			}
		}
	}
	return entries, nil
}

func (a *TmpFilesAnalysis) parseFile(path string, entries map[string]string) error {
	f, err := os.Open(path)
	if err != nil {
		return fmt.Errorf("opening %s: %w", path, err)
	}
	defer func() { _ = f.Close() }()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" || line[0] == '#' {
			continue
		}
		if p := extractPath(line); p != "" {
			entries[p] = line
		}
	}
	return scanner.Err()
}

// noRecursePaths директории, для которых создаётся tmpfiles.d запись,
// но содержимое не обходится (управляется другими механизмами).
var noRecursePaths = map[string]bool{
	"/var/home":      true,
	"/var/root":      true,
	"/var/cache/man": true,
	"/var/tmp":       true,
}

func (a *TmpFilesAnalysis) walk(rootfs, dir string) error {
	entries, err := os.ReadDir(dir)
	if err != nil {
		if os.IsPermission(err) || os.IsNotExist(err) {
			return nil
		}
		return fmt.Errorf("reading dir %s: %w", dir, err)
	}

	for _, entry := range entries {
		fullPath := filepath.Join(dir, entry.Name())

		relPath, err := filepath.Rel(rootfs, fullPath)
		if err != nil {
			return err
		}
		absPath := "/" + relPath

		// /var/run — симлинк на /run, управляется системой
		if absPath == "/var/run" {
			continue
		}

		canonPath := canonicalizePath(absPath)

		covered := a.Existing[canonPath] != ""

		info, err := entry.Info()
		if err != nil {
			return fmt.Errorf("stat %s: %w", fullPath, err)
		}

		switch ft := entry.Type(); {
		case ft&fs.ModeSymlink != 0:
			if covered {
				break
			}
			target, err := os.Readlink(fullPath)
			if err != nil {
				return fmt.Errorf("readlink %s: %w", fullPath, err)
			}
			escaped := escapePath(canonPath)
			a.Missing = append(a.Missing, TmpFilesEntry{
				Type: "L",
				Path: canonPath,
				Line: fmt.Sprintf("L %s - - - - %s", escaped, target),
			})

		case ft.IsDir():
			if !covered {
				mode := fmt.Sprintf("%04o", info.Mode().Perm())
				user, group := lookupOwner(info)
				escaped := escapePath(canonPath)
				a.Missing = append(a.Missing, TmpFilesEntry{
					Type:  "d",
					Path:  canonPath,
					Mode:  mode,
					User:  user,
					Group: group,
					Line:  fmt.Sprintf("d %s %s %s %s - -", escaped, mode, user, group),
				})
			}
			if !noRecursePaths[absPath] {
				if err = a.walk(rootfs, fullPath); err != nil {
					return err
				}
			}

		default:
			if !covered {
				a.Unsupported = append(a.Unsupported, canonPath)
			}
		}
	}
	return nil
}

// extractPath извлекает и каноникализирует путь из строки tmpfiles.d
func extractPath(line string) string {
	fields := tokenizeQuoted(line)
	if len(fields) < 2 {
		return ""
	}
	return canonicalizePath(unescapePath(fields[1]))
}

func canonicalizePath(p string) string {
	if strings.HasPrefix(p, "/var/run/") {
		return strings.TrimPrefix(p, "/var")
	}
	if p == "/var/run" {
		return "/run"
	}
	return p
}

func escapePath(s string) string {
	var b strings.Builder
	needsQuote := strings.ContainsAny(s, " \t")
	if needsQuote {
		b.WriteByte('"')
	}
	for _, c := range s {
		switch c {
		case '\n':
			b.WriteString(`\n`)
		case '\t':
			b.WriteString(`\t`)
		case '\r':
			b.WriteString(`\r`)
		case '\\':
			b.WriteString(`\\`)
		default:
			b.WriteRune(c)
		}
	}
	if needsQuote {
		b.WriteByte('"')
	}
	return b.String()
}

func unescapePath(s string) string {
	if !strings.ContainsRune(s, '\\') {
		return s
	}
	var b strings.Builder
	b.Grow(len(s))
	for i := 0; i < len(s); i++ {
		if s[i] != '\\' || i+1 >= len(s) {
			b.WriteByte(s[i])
			continue
		}
		switch s[i+1] {
		case 'x':
			if i+3 < len(s) {
				if v, err := strconv.ParseUint(s[i+2:i+4], 16, 8); err == nil {
					b.WriteByte(byte(v))
					i += 3
					continue
				}
			}
			b.WriteByte(s[i])
		case 'n':
			b.WriteByte('\n')
			i++
		case 't':
			b.WriteByte('\t')
			i++
		case 'r':
			b.WriteByte('\r')
			i++
		case '\\':
			b.WriteByte('\\')
			i++
		default:
			b.WriteByte(s[i])
		}
	}
	return b.String()
}
