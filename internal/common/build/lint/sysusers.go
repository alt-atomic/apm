package lint

import (
	"apm/internal/common/build/etcfiles"
	"apm/internal/common/reply"
	"bufio"
	"context"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
)

type sysusersEntryType int

const (
	sysusersUser sysusersEntryType = iota
	sysusersGroup
	sysusersRange
)

type sysusersEntry struct {
	Type       sysusersEntryType
	Name       string
	UID        *uint32 // user
	GID        *uint32 // user или group
	Gecos      string
	Home       string
	Shell      string
	RangeStart uint32
	RangeEnd   uint32
}

type sysusersAnalysis struct {
	MissingUsers  []etcfiles.PasswdEntry
	MissingGroups []etcfiles.GroupEntry
	Entries       []sysusersEntry
}

func (a *sysusersAnalysis) Analyze(ctx context.Context, rootfs string) error {
	reply.CreateEventNotification(ctx, reply.StateBefore, reply.WithEventName(reply.EventSystemLintSysusers))
	defer reply.CreateEventNotification(ctx, reply.StateAfter, reply.WithEventName(reply.EventSystemLintSysusers))
	entries, err := a.readEntries(rootfs)
	if err != nil {
		return fmt.Errorf("reading sysusers.d: %w", err)
	}
	a.Entries = entries

	coveredUsers := make(map[string]bool)
	coveredGroups := make(map[string]bool)
	for _, e := range entries {
		switch e.Type {
		case sysusersUser:
			coveredUsers[e.Name] = true
			coveredGroups[e.Name] = true
		case sysusersGroup:
			coveredGroups[e.Name] = true
		default:
		}
	}

	passwd, err := etcfiles.ParseColonFile(filepath.Join(rootfs, "etc", "passwd"), etcfiles.ParsePasswdLine)
	if err != nil {
		return fmt.Errorf("loading /etc/passwd: %w", err)
	}
	groups, err := etcfiles.ParseColonFile(filepath.Join(rootfs, "etc", "group"), etcfiles.ParseGroupLine)
	if err != nil {
		return fmt.Errorf("loading /etc/group: %w", err)
	}

	for _, p := range passwd {
		if !coveredUsers[p.Name] && etcfiles.IsSystemID(p.UID) {
			a.MissingUsers = append(a.MissingUsers, p)
		}
	}
	for _, g := range groups {
		if !coveredGroups[g.Name] && etcfiles.IsSystemID(g.GID) {
			a.MissingGroups = append(a.MissingGroups, g)
		}
	}

	sort.Slice(a.MissingUsers, func(i, j int) bool { return a.MissingUsers[i].Name < a.MissingUsers[j].Name })
	sort.Slice(a.MissingGroups, func(i, j int) bool { return a.MissingGroups[i].Name < a.MissingGroups[j].Name })
	return nil
}

func (a *sysusersAnalysis) MissingLines() []string {
	var lines []string

	userNames := make(map[string]bool, len(a.MissingUsers))
	for _, u := range a.MissingUsers {
		userNames[u.Name] = true
	}

	for _, g := range a.MissingGroups {
		if userNames[g.Name] {
			continue
		}
		lines = append(lines, fmt.Sprintf("g %s %d", g.Name, g.GID))
	}

	for _, u := range a.MissingUsers {
		gecos := optionalField(u.Gecos, true)
		home := optionalField(u.Home, false)
		shell := optionalField(u.Shell, false)
		lines = append(lines, fmt.Sprintf("u %s %d:%d %s %s %s", u.Name, u.UID, u.GID, gecos, home, shell))
	}
	return lines
}

func (a *sysusersAnalysis) GenerateConf() string {
	var b strings.Builder
	b.WriteString("# Auto-generated by apm lint\n")

	userNames := make(map[string]bool, len(a.MissingUsers))
	for _, u := range a.MissingUsers {
		userNames[u.Name] = true
	}

	// Группы без одноимённого пользователя
	for _, g := range a.MissingGroups {
		if userNames[g.Name] {
			continue
		}
		b.WriteString(fmt.Sprintf("g %s %d\n", g.Name, g.GID))
	}

	for _, u := range a.MissingUsers {
		gecos := optionalField(u.Gecos, true)
		home := optionalField(u.Home, false)
		shell := optionalField(u.Shell, false)
		b.WriteString(fmt.Sprintf("u %s %d:%d %s %s %s\n", u.Name, u.UID, u.GID, gecos, home, shell))
	}
	return b.String()
}

func (a *sysusersAnalysis) RemoveConf(rootfs string) (string, error) {
	return removeConf(rootfs, "sysusers.d")
}

// WriteConf записывает сгенерированный конфиг в rootfs/usr/lib/sysusers.d/apm-lint.conf
func (a *sysusersAnalysis) WriteConf(rootfs string) (string, error) {
	if len(a.MissingUsers) == 0 && len(a.MissingGroups) == 0 {
		return "", nil
	}
	return writeConf(rootfs, "sysusers.d", a.GenerateConf())
}

func (a *sysusersAnalysis) readEntries(rootfs string) ([]sysusersEntry, error) {
	dir := filepath.Join(rootfs, "usr", "lib", "sysusers.d")
	if _, err := os.Stat(dir); os.IsNotExist(err) {
		return nil, nil
	}

	files, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("reading %s: %w", dir, err)
	}

	var result []sysusersEntry
	seen := make(map[string]bool)

	for _, f := range files {
		if f.IsDir() || !strings.HasSuffix(f.Name(), ".conf") {
			continue
		}
		entries, err := a.parseFile(filepath.Join(dir, f.Name()))
		if err != nil {
			return nil, err
		}
		for _, e := range entries {
			if e.Type == sysusersRange || !seen[e.Name] {
				seen[e.Name] = true
				result = append(result, e)
			}
		}
	}
	return result, nil
}

func (a *sysusersAnalysis) parseFile(path string) ([]sysusersEntry, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, fmt.Errorf("opening %s: %w", path, err)
	}
	defer func() { _ = f.Close() }()

	var entries []sysusersEntry
	scanner := bufio.NewScanner(f)
	lineNum := 0

	for scanner.Scan() {
		lineNum++
		line := strings.TrimSpace(scanner.Text())
		if line == "" || line[0] == '#' {
			continue
		}
		entry, err := parseSysusersLine(line)
		if err != nil {
			return nil, fmt.Errorf("%s:%d: %w", path, lineNum, err)
		}
		if entry != nil {
			entries = append(entries, *entry)
		}
	}
	return entries, scanner.Err()
}

func parseSysusersLine(line string) (*sysusersEntry, error) {
	tokens := etcfiles.TokenizeQuoted(line)
	if len(tokens) < 2 {
		return nil, fmt.Errorf("too few fields: %s", line)
	}

	typ, name := tokens[0], tokens[1]

	switch typ {
	case "u", "u!":
		return parseUserEntry(name, tokens[2:])
	case "g":
		return parseGroupEntry(name, tokens[2:])
	case "r":
		return parseRangeEntry(name, tokens[2:])
	default:
		return nil, nil
	}
}

func parseUserEntry(name string, args []string) (*sysusersEntry, error) {
	entry := &sysusersEntry{Type: sysusersUser, Name: name}

	if len(args) > 0 && args[0] != "-" {
		if err := parseUserID(entry, args[0]); err != nil {
			return nil, err
		}
	}
	if len(args) > 1 && args[1] != "-" {
		entry.Gecos = args[1]
	}
	if len(args) > 2 && args[2] != "-" {
		entry.Home = args[2]
	}
	if len(args) > 3 && args[3] != "-" {
		entry.Shell = args[3]
	}
	return entry, nil
}

func parseUserID(entry *sysusersEntry, idStr string) error {
	if uidStr, gidStr, ok := strings.Cut(idStr, ":"); ok {
		if uid, err := parseOptionalID(uidStr); err != nil {
			return fmt.Errorf("invalid uid %q: %w", uidStr, err)
		} else {
			entry.UID = uid
		}
		if gid, err := parseOptionalID(gidStr); err != nil {
			return fmt.Errorf("invalid gid %q: %w", gidStr, err)
		} else {
			entry.GID = gid
		}
	} else if idStr[0] != '/' {
		uid, err := strconv.ParseUint(idStr, 10, 32)
		if err != nil {
			return fmt.Errorf("invalid uid %q: %w", idStr, err)
		}
		u32 := uint32(uid)
		entry.UID = &u32
	}
	return nil
}

func parseGroupEntry(name string, args []string) (*sysusersEntry, error) {
	entry := &sysusersEntry{Type: sysusersGroup, Name: name}
	if len(args) > 0 && args[0] != "-" && args[0][0] != '/' {
		gid, err := strconv.ParseUint(args[0], 10, 32)
		if err != nil {
			return nil, fmt.Errorf("invalid gid %q: %w", args[0], err)
		}
		g32 := uint32(gid)
		entry.GID = &g32
	}
	return entry, nil
}

func parseRangeEntry(_ string, args []string) (*sysusersEntry, error) {
	if len(args) < 1 {
		return nil, fmt.Errorf("range entry requires start-end")
	}
	startStr, endStr, ok := strings.Cut(args[0], "-")
	if !ok {
		return nil, fmt.Errorf("invalid range format %q", args[0])
	}
	start, err := strconv.ParseUint(startStr, 10, 32)
	if err != nil {
		return nil, fmt.Errorf("invalid range start: %w", err)
	}
	end, err := strconv.ParseUint(endStr, 10, 32)
	if err != nil {
		return nil, fmt.Errorf("invalid range end: %w", err)
	}
	return &sysusersEntry{
		Type:       sysusersRange,
		RangeStart: uint32(start),
		RangeEnd:   uint32(end),
	}, nil
}

func parseOptionalID(s string) (*uint32, error) {
	if s == "" || s == "-" || s[0] == '/' {
		return nil, nil
	}
	// Имя группы (не число, не путь) — валидная ссылка, просто без числового id
	if s[0] < '0' || s[0] > '9' {
		return nil, nil
	}
	v, err := strconv.ParseUint(s, 10, 32)
	if err != nil {
		return nil, err
	}
	u32 := uint32(v)
	return &u32, nil
}

func optionalField(s string, quote bool) string {
	if s == "" {
		return "-"
	}
	if quote && strings.ContainsAny(s, " \t") {
		return fmt.Sprintf("%q", s)
	}
	return s
}
